import networkx as nx
edge = adult[['education', 'occupation']].dropna().drop_duplicates()
G=nx.from_pandas_edgelist(edge,'education', 'occupation')
pos=nx.spring_layout(G,k=10,)
# spring_layout is a graph layout algorithm in NetworkX that determines the positions (x, y coordinates) of nodes in a graph before drawing it.
nx.draw(G,pos,with_labels=True,node_size=700,font_size=9)

