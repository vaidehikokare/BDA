import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
import plotly.express as px
import networkx as nx

# Load datasets
iris = sns.load_dataset('iris')

# Load Adult dataset from UCI (or local CSV)
adult = pd.read_csv(
    'https://archive.ics.uci.edu/ml/machine-learning-databases/adult/adult.data',
    header=None,
    names=['age','workclass','fnlwgt','education','education-num','marital-status',
           'occupation','relationship','race','sex','capital-gain','capital-loss',
           'hours-per-week','native-country','income']
)

plt.figure(figsize=(7,5))
sns.histplot(iris['sepal_length'], bins=20, kde=True, color='skyblue')
plt.title("1D: Distribution of Sepal Length (Iris)")
plt.xlabel("Sepal Length (cm)")
plt.show()

sns.scatterplot(x='sepal_length', y='petal_length', hue='species', data=iris)
plt.title("2D: Sepal vs Petal Length (Iris)")
plt.show()

import plotly.express as px

fig=px.scatter_3d(
    x=iris['sepal_length'],
    y=iris['petal_width'],
    z=iris['sepal_width'],
    color=iris['species'],
    size=iris['petal_length']
)

fig

